v <- c(2, 5.5, 6)
t <- c(8, 3, 4)


cat("add vectors: ", v, "and", t, "is", v+t)
cat("subtract two vectors: ", v, "and", t, "is", v-t)
cat("multiple two vectors: ", v, "and", t, "is", v*t)
cat("divide tow vectors: ", v, "and", t, "is", v/t)
cat("get reminder of the vectors:", v, "and", t, "is", v%%t)
cat("get division quotient of the vectors: ", v, "and", t, "is", v%%t)
cat("first vector raised to the component of the second vector:", v, "and", t, "is", v^t)